func TestCall_Retries503WithExponentialBackoff(t *testing.T) {
	var attempts atomic.Int32

	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		n := attempts.Add(1)
		if n <= 2 {
			// First 2 attempts return 503
			w.Header().Set("Content-Type", "application/json")
			w.WriteHeader(503)
			json.NewEncoder(w).Encode(GatewayError{
				Code:    "circuit_open",
				Message: "Circuit breaker is open",
			})
			return
		}
		// 3rd attempt succeeds
		w.Header().Set("Content-Type", "application/json")
		json.NewEncoder(w).Encode(jsonRPCResponse{
			JSONRPC: "2.0",
			Result:  map[string]any{"status": "ok"},
			ID:      float64(1),
		})
	}))
	defer srv.Close()

	var sleepDurations []time.Duration
	c := NewClient(srv.URL, "spiffe://test",
		WithHTTPClient(srv.Client()),
		WithMaxRetries(3),
		WithBackoffBase(100*time.Millisecond),
		withSleepFunc(func(d time.Duration) { sleepDurations = append(sleepDurations, d) }),
	)

	result, err := c.Call(context.Background(), "tool", nil)
	if err != nil {
		t.Fatalf("Call failed: %v", err)
	}
	if result == nil {
		t.Fatal("expected non-nil result")
	}

	// Should have attempted 3 times (2 retries + 1 success)
	if got := attempts.Load(); got != 3 {
		t.Errorf("attempts = %d, want 3", got)
	}

	// Should have slept twice (between attempt 1->2 and 2->3)
	if len(sleepDurations) != 2 {
		t.Fatalf("sleep count = %d, want 2", len(sleepDurations))
	}

	// Exponential: 100ms, 200ms
	if sleepDurations[0] != 100*time.Millisecond {
		t.Errorf("sleep[0] = %v, want %v", sleepDurations[0], 100*time.Millisecond)
	}
	if sleepDurations[1] != 200*time.Millisecond {
		t.Errorf("sleep[1] = %v, want %v", sleepDurations[1], 200*time.Millisecond)
	}
}

